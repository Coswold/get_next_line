#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*new;

	i = 0;
	while (s1[i] != '\0')
		i++;
	if (NULL == (new = (char*)malloc(sizeof(char) * i + 1)))
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
