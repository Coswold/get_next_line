#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (0);
	while (j < n && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
		j++;
	}
	return (1);
}
